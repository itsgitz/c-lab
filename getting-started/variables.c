#include <stdio.h>

int
main ()
{
  int myNumber = 15;
  float myFloatNumber = 5.99;
  char myLetter = 'D';
  int x = 5;
  int y = 10;
  int sum = x + y;

  printf ("Let's learn variables! \n");
  printf ("My number: %d \n", myNumber);
  printf ("My float number: %f\n", myFloatNumber);
  printf ("My letter: %c\n", myLetter);
  printf ("x + y = %d \n", sum);

  return 0;
}
